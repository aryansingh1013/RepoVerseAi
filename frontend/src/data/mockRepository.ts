/**
 * Derive the scale of a folder star from its file count.
 * More files → bigger, brighter star. Clamped so outliers don't blow out.
 */
function folderScale(fileCount: number): number {
  return 0.35 + Math.min(fileCount / 10, 1) * 0.55;
}

